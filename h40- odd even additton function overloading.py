##odd even addision using function overloading
#'ans+ add +10 & ans+ add -10' Using function ovrloading
class over:
    def over1(self,t,*v):
        if t=='add_ans+10':
            s=10
        if t=='add_ans-10':
            s=-10
        for i in v:
            s+=i
        print(s)
           
obj=over()
obj.over1('add_ans+10',10,20,30)
obj.over1('add_ans-10',10,20,30)
